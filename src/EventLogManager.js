import { DataManager } from './dataManager/dataManager.js';

export const PHASE_SEQUENCE = (() => {
  const seq = [];
  for (let n = 1; n < 20; n++) {
    seq.push(`night_${n}`, `day_${n + 1}`);
  }
  return seq;
})();

